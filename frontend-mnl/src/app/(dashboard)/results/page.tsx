                        const reportFiles = filesByReport[report.report_id] ?? [];
                        const primaryFile = reportFiles.find((file) => file.file_id === report.primary_file_id) ?? reportFiles[0] ?? null;
                        const canGenerate = report.source === "qudrugforge" && ["draft", "queued", "failed"].includes(report.status);

